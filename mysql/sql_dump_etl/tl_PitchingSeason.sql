CREATE TABLE `PitchingSeason` (
    `playerID` VARCHAR(10),
    `teamID` VARCHAR(4),
    `year` INT,
    `W` SMALLINT,
    `SV` SMALLINT,
    `IPOuts` SMALLINT,
    `SO` SMALLINT,
	`ER` SMALLINT,
    `ERA` FLOAT,
    PRIMARY KEY (`playerID`, `teamID`, `year`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci;
INSERT INTO `PitchingSeason` (playerID, teamID, year, W, SV, IPOuts, SO, ER, ERA)
SELECT
	playerID,
	teamID,
	yearID AS year,
	SUM(W) AS W,
	SUM(SV) AS SV,
	SUM(IPOuts) AS IPOuts,
	SUM(SO) AS SO,
	SUM(ER) AS ER,
	CASE
		WHEN SUM(IPOuts) = 0 AND SUM(ER) = 0 THEN 0.000
		WHEN SUM(IPOuts) = 0 AND SUM(ER) != 0 THEN NULL
		ELSE ROUND(SUM(ER) / SUM(IPOuts) * 27, 3)
	END AS ERA
FROM Pitching
GROUP BY playerID, yearID, teamID;

